#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
    int largestSubmatrix(vector<vector<int>> &matrix)
    {
        int n = matrix.size(), m = matrix[0].size();
        int ans = 0;

        for (int row = 0; row < n; row++)
        {
            // count block of `1` for column
            if (row)
            {
                for (int col = 0; col < m; col++)
                {
                    if (matrix[row][col] != 0)
                    {
                        matrix[row][col] += matrix[row - 1][col];
                    }
                }
            }

            // make a copy of the last row
            vector<int> lastRow = matrix[row];

            // // sort this row
            sort(lastRow.begin(), lastRow.end(), greater<int>());

            // loop through column length
            for (int i = 0; i < m; i++)
            {
                int length_of_row = (i + 1);
                int value = (lastRow[i] * length_of_row);

                ans = max(ans, value);
            }
        }

        return ans;
    }
};

